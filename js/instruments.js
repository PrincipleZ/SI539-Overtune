function growUp(elem){
  var $child = $(elem).children()[0];
  var $newHeight = parseInt(jQuery($child).css("height"))* 1.5;
  jQuery($child).animate({width: "9vw", bottom: $newHeight-175 + "px"}, 300);
}

function growDown(elem){
  var $child = $(elem).children()[0];
  jQuery($child).stop();
  jQuery($child).animate({width:"6vw", bottom: "0"}, 0 );

}
